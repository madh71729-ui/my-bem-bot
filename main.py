@bot.message_handler(commands=['start'])
def start(m):
    kb = types.InlineKeyboardMarkup(row_width=1)
    kb.add(
        types.InlineKeyboardButton("📝 تحدي الـ 15 سؤالاً (استنتاجي)", callback_data="sec_ex"),
        types.InlineKeyboardButton("💡 قسم المعارف الذهبية", callback_data="sec_kn"),
        types.InlineKeyboardButton("📚 بنك الشهادات", url="https://www.dzexams.com/ar/4am")
    )
    bot.send_message(m.chat.id, "🦅 **مراجعة 4am2 bem2026**\nالبوت مضبوط لـ 8 مواد بـ 15 سؤالاً استنتاجياً لكل مادة!", reply_markup=kb)

@bot.callback_query_handler(func=lambda call: True)
def handle(call):
    cid, mid = call.message.chat.id, call.message.message_id
    if call.data == "sec_ex":
        kb = types.InlineKeyboardMarkup(row_width=2)
        for k in DATA: kb.add(types.InlineKeyboardButton(DATA[k]["n"], callback_data=f"ex_{k}"))
        kb.add(types.InlineKeyboardButton("🔙 رجوع", callback_data="home"))
        bot.edit_message_text("📖 اختر المادة (15 س استنتاجي):", cid, mid, reply_markup=kb)
    elif call.data == "sec_kn":
        kb = types.InlineKeyboardMarkup(row_width=2)
        for k in DATA: kb.add(types.InlineKeyboardButton(f"💡 {DATA[k]['n']}", callback_data=f"kn_{k}"))
        kb.add(types.InlineKeyboardButton("🔙 رجوع", callback_data="home"))
        bot.edit_message_text("💡 معارف استنتاجية هامة:", cid, mid, reply_markup=kb)
    elif call.data.startswith("ex_"):
        c = call.data.split("_")[1]
        kb = types.InlineKeyboardMarkup().add(types.InlineKeyboardButton("👁️ مفاتيح الحل", callback_data=f"sol_{c}"), types.InlineKeyboardButton("🔙", callback_data="sec_ex"))
        bot.edit_message_text(DATA[c]["q"], cid, mid, reply_markup=kb)
    elif call.data.startswith("sol_"):
        c = call.data.split("_")[1]
        bot.edit_message_text(DATA[c]["a"], cid, mid, reply_markup=types.InlineKeyboardMarkup().add(types.InlineKeyboardButton("🔙 للأسئلة", callback_data=f"ex_{c}")))
    elif call.data.startswith("kn_"):
        c = call.data.split("_")[1]
        bot.edit_message_text(DATA[c]["k"], cid, mid, reply_markup=types.InlineKeyboardMarkup().add(types.InlineKeyboardButton("🔙 رجوع", callback_data="sec_kn")))
    elif call.data == "home": start(call.message)

if __name__ == '__main__':
    keep_alive() # لتشغيل سيرفر الويب المساعد لـ Render
    bot.polling(none_stop=True)
