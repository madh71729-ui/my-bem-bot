# --- 1. جزء الحفاظ على البوت مستيقظاً (Keep Alive) ---
app = Flask('')

@app.route('/')
def home():
    return "البوت يعمل بنجاح!"

def run():
    # المنصة ستقوم بتعيين Port تلقائياً أو نستخدم 8080
    port = int(os.environ.get("PORT", 8080))
    app.run(host='0.0.0.0', port=port)

def keep_alive():
    t = Thread(target=run)
    t.start()

