@bot.message_handler(commands=['start'])
def start(m):
    kb = types.InlineKeyboardMarkup(row_width=1)
    kb.add(
        types.InlineKeyboardButton("🔥 اختبارات المستوى العالي (BEM)", callback_data="sec_ex"),
        types.InlineKeyboardButton("💡 موسوعة المعارف", callback_data="sec_kn"),
        types.InlineKeyboardButton("📚 المصادر الرسمية", url="https://www.dzexams.com/ar/4am")
    )
    bot.send_message(m.chat.id, "🦅 **مرحباً بك في مراجعة 4am2 bem2026**\nتم رفع مستوى الصعوبة إلى الحد الأقصى. هل أنت مستعد؟", reply_markup=kb)

@bot.callback_query_handler(func=lambda call: True)
def handle(call):
    cid, mid = call.message.chat.id, call.message.message_id
    if call.data == "sec_ex":
        kb = types.InlineKeyboardMarkup(row_width=2)
        for k in DATA: kb.add(types.InlineKeyboardButton(DATA[k]["n"], callback_data=f"ex_{k}"))
        kb.add(types.InlineKeyboardButton("🔙 رجوع", callback_data="home"))
        bot.edit_message_text("📖 اختر المادة (أسئلة للممتازين فقط):", cid, mid, reply_markup=kb)
    elif call.data == "sec_kn":
        kb = types.InlineKeyboardMarkup(row_width=2)
        for k in DATA: kb.add(types.InlineKeyboardButton(f"💡 {DATA[k]['n']}", callback_data=f"kn_{k}"))
        kb.add(types.InlineKeyboardButton("🔙 رجوع", callback_data="home"))
        bot.edit_message_text("💡 معارف دقيقة للاختبارات:", cid, mid, reply_markup=kb)
    elif call.data.startswith("ex_"):
        c = call.data.split("_")[1]
        kb = types.InlineKeyboardMarkup().add(types.InlineKeyboardButton("👁️ كشف الحل النموذجي", callback_data=f"sol_{c}"),
                                              types.InlineKeyboardButton("🔙", callback_data="sec_ex"))
        bot.edit_message_text(DATA[c]["q"], cid, mid, reply_markup=kb)
    elif call.data.startswith("sol_"):
        c = call.data.split("_")[1]
        kb = types.InlineKeyboardMarkup().add(types.InlineKeyboardButton("🔙 رجوع للأسئلة", callback_data=f"ex_{c}"))
        bot.edit_message_text(DATA[c]["a"], cid, mid, reply_markup=kb)
    elif call.data.startswith("kn_"):
        c = call.data.split("_")[1]
        kb = types.InlineKeyboardMarkup().add(types.InlineKeyboardButton("🔙 رجوع", callback_data="sec_kn"))
        bot.edit_message_text(DATA[c]["k"], cid, mid, reply_markup=kb)
    elif call.data == "home": start(call.message)

